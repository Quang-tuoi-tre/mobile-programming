void main() {
  // Khai báo các kiểu dữ liệu
  int soNguyen = 10; // Số nguyên
  double soThuc = 15.5; // Số thực
  String chuoiKyTu = "Xin chào!"; // Chuỗi ký tự
  bool giaTriBoolean = true; // Giá trị boolean

  // In giá trị của các biến ra console
  print("Giá trị của số nguyên là: $soNguyen");
  print("Giá trị của số thực là: $soThuc");
  print("Giá trị của chuỗi ký tự là: $chuoiKyTu");
  print("Giá trị của boolean là: $giaTriBoolean");
}
